////Increment Operator
//In JavaScript we use the increment operator to increase a value stored in a variable. 
//The increment could be pre or post increment. Let us see each of them:

//Examples

//Pre Increment
let count = 0
console.log(++count)        // 1
console.log(count)          // 1

//Post Increment
let count = 0
console.log(count++)        // 0
console.log(count)          // 1